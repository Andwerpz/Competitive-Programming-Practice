signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    int t;
    cin >> t;
    while(t--){
        int n, k;
        cin >> n >> k;
        int ptr = k;
        for(int i = 0; i < n; i++){
            cout << ptr << " ";
            ptr --;
            if(ptr == 0) ptr = k;
        }
        cout << "\n";
    }
    
    return 0;
}