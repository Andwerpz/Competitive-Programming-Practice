//AtCoder - ARC187B

//when are there multiple components? If you can split the array into a left and right half where the left half minimum is
//greater than the right half maximum, then the left and right half are not connected, therefore they are two different
//components. Since each such splitting position will contribute one component to the count, let's count for each splitting
//position, the number of arrays where the above condition is true. 

//For some specific splitting position, we can iterate over the prefix minimum. Then, for each prefix minimum, we can count
//the number of ways to assign values s.t. the prefix has exactly that minimum, and all the suffix entries are strictly less
//than the prefix minimum. 

//Don't forget to consider that each graph contributes one component minimum, so add M^Q to the answer as well. 

struct mint;
typedef vector<mint> vm;
typedef vector<vector<mint>> vvm;
typedef pair<mint, mint> pmm;

const ll mod = 998244353;
struct mint {
    ll val; //this should always be in range [0, mod)
    mint(ll _val = 0) {val = _val; if(val < 0) val = mod + (val % mod);}
    mint(const mint& other) {val = other.val;}
    bool operator ==(const mint& other) const {return val == other.val;}
    bool operator ==(ll other) const {return val == other;}
    bool operator !=(const mint& other) const {return val != other.val;}
    bool operator !=(ll other) const {return val != other;}
    bool operator >(const mint& other) const {return val > other.val;}
    bool operator >(ll other) const {return val > other;}
    bool operator <(const mint& other) const {return val < other.val;}
    bool operator <(ll other) const {return val < other;}
    mint& operator =(const mint& other) {val = other.val; return *this;}
    mint& operator =(ll other) {val = other; return *this;}
    mint operator +(const mint& other) const {ll ret = val + other.val; while(ret >= mod) {ret -= mod;} return mint(ret);}
    mint operator +(ll other) const {return *this + mint(other);}
    mint& operator +=(const mint& other) {*this = *this + other; return *this;}
    mint& operator +=(ll other) {*this = *this + other; return *this;}
    mint operator -(const mint& other) const {ll ret = val - other.val; while(ret < 0) {ret += mod;} return mint(ret);}
    mint operator -(ll other) const {return *this - mint(other);}
    mint& operator -=(const mint& other) {*this = *this - other; return *this;}
    mint& operator -=(ll other) {*this = *this - other; return *this;}
    mint operator *(const mint& other) const {return mint((val * other.val) % mod);}
    mint operator *(ll other) const {return *this * mint(other);}
    mint& operator *=(const mint& other) {*this = *this * other; return *this;}
    mint& operator *=(ll other) {*this = *this * other; return *this;}
    mint operator /(const mint& other) const {return mint((val / other.val) % mod);}
    mint operator /(ll other) const {return *this / mint(other);}
    mint& operator /=(const mint& other) {*this = *this / other; return *this;}
    mint& operator /=(ll other) {*this = *this / other; return *this;}
    mint operator %(const mint& other) const {return mint(val % other.val);}
    mint operator %(ll other) const {return *this % mint(other);}
    mint& operator %=(const mint& other) {*this = *this % other; return *this;}
    mint& operator %=(ll other) {*this = *this % other; return *this;}

    //don't forget about fermat's little theorem, 
    //a^(m-1) % m = 1. This means that a^(p % m) % m != a^(p) % m, rather a^(p % (m-1)) % m = a^(p) % m. 
    mint pow(const mint& other) const {
        mint ans(1), p(val);
        ll b = other.val;
        while(b != 0) {if(b % 2 == 1) {ans *= p;} p *= p; b /= 2;}
        return ans;
    }
    mint pow(ll other) const {return this->pow(mint(other));}

    //returns X such that A = (B * X) % mod
    mint inv_divide(const mint& other) const {
        return *this * other.pow(mod - 2);
    }

    friend std::ostream& operator<<(std::ostream& os, const mint& m) {os << m.val; return os;}
    friend std::istream& operator>>(std::istream& is, mint& m) {is >> m.val; return is;}
    operator size_t() const {return val;}
};
bool operator ==(ll a, const mint& b) {return mint(a) == b;}
bool operator !=(ll a, const mint& b) {return mint(a) != b;}
bool operator >(ll a, const mint& b) {return mint(a) > b;}
bool operator <(ll a, const mint& b) {return mint(a) < b;}
mint operator +(ll a, const mint& b) {return mint(a) + b;}
mint operator -(ll a, const mint& b) {return mint(a) - b;}
mint operator *(ll a, const mint& b) {return mint(a) * b;}
mint operator /(ll a, const mint& b) {return mint(a) / b;}
mint operator %(ll a, const mint& b) {return mint(a) % b;}

vm fac, invfac;
void fac_init(int N) {
    fac = vm(N + 1), invfac = vm(N + 1);
    fac[0] = 1;
    for(int i = 1; i <= N; i++) fac[i] = fac[i - 1] * i;
    invfac[N] = mint(1).inv_divide(fac[N]);
    for(int i = N; i > 0; i--) invfac[i - 1] = invfac[i] * i;
}

//n >= k
mint nck(mint n, mint k) {
    return fac[n] * invfac[k] * invfac[n - k];
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    fac_init(3000);
    int n, m;
    cin >> n >> m;
    vi a(n);
    for(int i = 0; i < n; i++) cin >> a[i];
    int sncnt = 0;
    for(int i = 0; i < n; i++) sncnt += a[i] == -1;
    vi sfx_mx(n + 1, 1), pfx_mn(n + 1, m);
    for(int i = n - 1; i >= 0; i--) sfx_mx[i] = max(sfx_mx[i + 1], a[i]);
    for(int i = 1; i <= n; i++) pfx_mn[i] = min(pfx_mn[i - 1], (a[i - 1] == -1? (int) 1e9 : a[i - 1]));
    mint ans = mint(m).pow(sncnt);
    int pncnt = 0;
    for(int i = 0; i < n - 1; i++){
        sncnt -= a[i] == -1, pncnt += a[i] == -1;
        //iterate over all possible mpfx, and count how we can assign msfx
        for(int j = sfx_mx[i + 1] + 1; j <= pfx_mn[i + 1]; j++){
            mint pfx_ways = mint(m - j + 1).pow(pncnt);
            if(j != pfx_mn[i + 1]) pfx_ways -= mint(m - j).pow(pncnt);
            ans += pfx_ways * mint(j - 1).pow(sncnt);
        }
    }
    cout << ans << "\n";
    
    return 0;
}