bool operator <(ll a, const mint& b) {return a < b.val;}
mint operator +(ll a, const mint& b) {ll ret = a + b.val; while(ret >= mod) {ret -= mod;} return mint(ret);}
mint operator -(ll a, const mint& b) {ll ret = a - b.val; while(ret < 0) {ret += mod;} return mint(ret);}
mint operator *(ll a, const mint& b) {return mint((a * b.val) % mod);}
mint operator /(ll a, const mint& b) {return mint((a / b.val) % mod);}
mint operator %(ll a, const mint& b) {return mint(a % b.val);}

mint solve(int step, int cnt, mint p, vvb& v, vvm& dp, int n){
    //we've inserted step pairs, and there are cnt valid positions to place ')('
    if(step == n){
        return cnt != 0? 1 : 0;
    }
    if(v[step][cnt]){
        return dp[step][cnt];
    }
    int totpos = step * 2 + 1;
    mint p_valid = mint(cnt).inv_divide(totpos);
    cout << "SOLVE : " << step << " " << cnt << "\n";
    cout << "P_VALID : " << p_valid << "\n";
    mint ans = 0;
    //insert '()';
    {
        //insert it into a valid position
        ans += p * p_valid * solve(step + 1, cnt + 2, p, v, dp, n);
        //insert it into a invalid position
        ans += p * (1 - p_valid) * solve(step + 1, cnt + 1, p, v, dp, n);
    }
    //insert ')('
    ans += (1 - p) * p_valid * solve(step + 1, cnt + 1, p, v, dp, n);
    dp[step][cnt] = ans;
    v[step][cnt] = true;
    return ans;
}

