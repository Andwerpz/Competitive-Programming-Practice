signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        for(int i = 0; i < n - 1; i++) {
            cout << "0";
        }
        cout << "1\n";
    }
    
    return 0;
}