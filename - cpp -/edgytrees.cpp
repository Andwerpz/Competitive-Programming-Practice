struct DSU {
    int N;
    vector<int> dsu, sz;

    DSU(int n) {
        this->N = n;
        this->dsu = vector<int>(n, 0);
        this->sz = vector<int>(n, 1);
        for(int i = 0; i < n; i++){ //initialize roots
            dsu[i] = i;
        }
    }

    int find(int a) {
        if(dsu[a] == a) {
            return a;
        }
        return dsu[a] = find(dsu[a]);
    }

    int get_sz(int a){
        return sz[find(a)];
    }

    //ret true if updated something
    bool unify(int a, int b) {
        int ra = find(a);
        int rb = find(b);
        if(ra == rb) {
            return false;
        }
        dsu[rb] = ra;
        sz[ra] += sz[rb];
        return true;
    }
};

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);
    
    int n, k;
    cin >> n >> k;
    DSU dsu(n);
    for(int i = 0; i < n - 1; i++){
        int u, v, col;
        cin >> u >> v >> col;
        u --;
        v --;
        if(!col) dsu.unify(u, v);
    }
    mint ans = mint(n).pow(k);
    vb v(n, false);
    for(int i = 0; i < n; i++){
        int p = dsu.find(i);
        if(v[p]) continue;
        ans -= mint(dsu.sz[p]).pow(k);
        v[p] = true;
    }
    cout << ans << "\n";
    
    return 0;
}